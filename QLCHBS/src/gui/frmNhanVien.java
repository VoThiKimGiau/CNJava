    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:

        int row = tbNhanVien.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn hàng cần xóa");
        } else {
            int n = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không?", "Cảnh báo", JOptionPane.YES_NO_OPTION);
            if (n == JOptionPane.NO_OPTION) {
                return;
            } else {
                if (NhanVienDAO.deleteNV(txtMaNV.getText().toString())) {
                    dsNV = NhanVienDAO.layDSNV();
                    JOptionPane.showMessageDialog(this, "Xóa thành công");
                } else {
                    JOptionPane.showMessageDialog(this, "Xóa thất bại. Vui lòng thử lại");
                }
                hienThi(dsNV);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        txtMaNV.setText("");
        txtTenNV.setText("");
        txtDiaChi.setText("");

        txtTenNV.enable(false);
        txtDiaChi.enable(false);
        cboChucVu.enable(false);
        txtSDT.enable(true);
        txtSDT.requestFocus();

        if (txtSDT.getText().isEmpty())
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số điện thoại");
        else {
            if (kiemTraNhap(1)) {
                ArrayList<NhanVien> lst = new ArrayList<>();
                NhanVien nv = NhanVienDAO.searchSDT(txtSDT.getText().toString());
                if (nv.getMaNV() == null) {
                    JOptionPane.showMessageDialog(this, "Không có khách hàng có số điện thoại này!");
                } else {
                    lst.add(nv);
                    hienThi(lst);
                }
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        hienThi(dsNV);
        NhanVien nv = new NhanVien();
        nv.setTenNV(txtTenNV.getText().toString());
        nv.setDiaChi(txtDiaChi.getText().toString());
        nv.setSdt(txtSDT.getText().toString());
        nv.setChucVu(cboChucVu.getSelectedItem().toString());

        if (txtMaNV.getText().isEmpty()) {
            if (kiemTraNhap(0)) {
                if (NhanVienDAO.themNV(nv)) {
                    dsNV = NhanVienDAO.layDSNV();
                    JOptionPane.showMessageDialog(this, "Thêm nhân viên thàng công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm nhân viên thất bại. Vui lòng thử lại", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        } else {
            if (kiemTraNhap(0)) {
                nv.setMaNV(txtMaNV.getText().toString());
                if (NhanVienDAO.updateNV(nv)) {
                    dsNV = NhanVienDAO.layDSNV();
                    JOptionPane.showMessageDialog(this, "Cập nhật nhân viên thàng công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Cập nhật nhân viên thất bại. Vui lòng thử lại", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }

        hienThi(dsNV);
    }//GEN-LAST:event_btnSaveActionPerformed

