    private void LoadData() {
        try {
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Mã danh mục");
            model.addColumn("Tên danh mục");

            List<DanhMucSach> ds = DanhMucSachDAO.getAllCategories();

            for (DanhMucSach danhmuc : ds) {
                Object[] row = {
                    danhmuc.getMaDanhMuc(),
                    danhmuc.getTenDanhMuc()
                };
                model.addRow(row);
            }

            // Set model cho jTable1
            jTable1.setModel(model);
        } catch (Exception ex) {
            ex.printStackTrace();
            // Xử lý lỗi nếu cần
        }
        txtMaDanhMuc.setText("");
        txtTenDanhMuc.setText("");
    }

