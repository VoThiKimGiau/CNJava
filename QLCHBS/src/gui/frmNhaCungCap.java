    public void hienThi(ArrayList<NhaCungCap> ds) {
        dtm.setRowCount(0);
        for (NhaCungCap ncc : ds) {
            Vector<Object> vec = new Vector<Object>();
            vec.add(ncc.getMaNCC());
            vec.add(ncc.getTenNCC());
            vec.add(ncc.getDiaChi());
            vec.add(ncc.getSdt());
            vec.add(ncc.getEmail());
            dtm.addRow(vec);
        }
    }

    public boolean kiemTraNhap(int loai) {
        boolean kq = true;
        String sdt = txtSDTNCC.getText().toString();
        String email = txtEmailNCC.getText().toString();
        NhaCungCap ncc = NhaCungCapDAO.searchSDT(sdt);
        NhaCungCap nccE = NhaCungCapDAO.searchEmail(email);

        if (sdt.isEmpty()) {
            kq = false;
            JOptionPane.showMessageDialog(this, "Số điện thoại không được bỏ trống. Vui lòng nhập lại!");
            txtSDTNCC.requestFocus();
        } else if (sdt.matches(".*\\D.*")) {
            kq = false;
            JOptionPane.showMessageDialog(this, "Số điện thoại không được có chữ. Vui lòng nhập lại!");
            txtSDTNCC.requestFocus();
        } else if (!sdt.substring(0, 1).equals("0")) {
            kq = false;
            JOptionPane.showMessageDialog(this, "Số điện thoại không bắt đầu bằng 0. Vui lòng nhập lại!");
            txtSDTNCC.requestFocus();
        } else if (sdt.length() < 10) {
            kq = false;
            JOptionPane.showMessageDialog(this, "Số điện thoại không đủ 10 số. Vui lòng nhập lại!");
            txtSDTNCC.requestFocus();
        } else if (sdt.length() > 11) {
            kq = false;
            JOptionPane.showMessageDialog(this, "Số điện thoại hơn 11 số. Vui lòng nhập lại!");
            txtSDTNCC.requestFocus();
        } else if (ncc.getMaNCC() != null && txtMaNCC.getText().toString().isEmpty() && loai == 0) {// loại = 0 là không phải tìm kiếm
            kq = false;
            JOptionPane.showMessageDialog(this, "Số điện thoại đã tồn tại. Vui lòng nhập lại!");
            txtSDTNCC.requestFocus();
        } else if (txtDiaChiNCC.getText().toString().isEmpty() && loai == 0) {
            kq = false;
            txtDiaChiNCC.setText("0");
            JOptionPane.showMessageDialog(this, "Địa chỉ không được để trống. Nếu mua tại chỗ không cần giao vui lòng nhập 0.\nVui lòng nhập lại!");
            return kq;
        } else if (nccE.getMaNCC() == null || nccE.getEmail().equals("")) {
            kq = true;
            return kq;
        } else if (!email.contains("@")) {
            kq = false;
            JOptionPane.showMessageDialog(this, "Emal phải có @. Vui lòng nhập lại!");
            txtEmailNCC.requestFocus();
        } else if (email.length() == 1) {
            kq = false;
            JOptionPane.showMessageDialog(this, "Email phải có kí tự khác ngoài @. Vui lòng nhập lại!");
            txtEmailNCC.requestFocus();
        } else if (nccE.getMaNCC() != null && txtMaNCC.getText().toString().isEmpty()) {
            kq = false;
            JOptionPane.showMessageDialog(this, "Email đã tồn tại. Vui lòng nhập lại!");
            txtEmailNCC.requestFocus();
        }

        return kq;
    }

