    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        txt_maSach.setEnabled(true);
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            String maSach = (String) jTable1.getValueAt(selectedRow, 0);
            String tenSach = (String) jTable1.getValueAt(selectedRow, 1);
            Integer sl = (Integer) jTable1.getValueAt(selectedRow, 2);
            Double giaBan = (Double) jTable1.getValueAt(selectedRow, 3);

            txt_maSach.setText(maSach);
            txt_maSach.enable(false);
            txt_tenSach.setText(tenSach);
            txt_sl.setText(sl.toString());
            txt_giaBan.setText(giaBan.toString());
        }
    }//GEN-LAST:event_jTable1MouseClicked

