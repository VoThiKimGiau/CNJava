    private void btnTKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTKActionPerformed
        // TODO add your handling code here:
        var btn = btnGroup.getSelection();
        String data;
        if (cboData.getSelectedItem() == null) {
            data = "";
        } else {
            data = cboData.getSelectedItem().toString();
        }

        if (btn != null) {
            if (btn == rdMonth.getModel()) {
                ArrayList<String> ds = new ArrayList<>();
                try {
                    if (data.equals("All")) {

                        ArrayList<String> mon = ThongKeDAO.layThang();
                        for (String x : mon) {
                            Double kq = ThongKeDAO.thongKeTheoThang(Integer.parseInt(x));

                            ds.add(x);
                            ds.add(String.valueOf(kq));
                        }
                    } else {
                        Double kq = ThongKeDAO.thongKeTheoThang(Integer.parseInt(data));
                        ds.add(data);
                        ds.add(String.valueOf(kq));
                    }
                    String[] tieuDe = {"Tháng", "Doanh thu"};
                    dtm.setColumnIdentifiers(tieuDe);
                    hienThiDoanhThu(ds);
                } catch (SQLException ex) {
                    Logger.getLogger(frmThongKe.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else if (btn == rdYear.getModel()) {
                ArrayList<String> ds = new ArrayList<>();
                try {
                    if (data.equals("All")) {

                        ArrayList<String> yy = ThongKeDAO.layNam();
                        for (String x : yy) {
                            Double kq = ThongKeDAO.thongKeTheoNam(Integer.parseInt(x));

                            ds.add(x);
                            ds.add(String.valueOf(kq));
                        }
                    } else {
                        Double kq = ThongKeDAO.thongKeTheoNam(Integer.parseInt(data));
                        ds.add(data);
                        ds.add(String.valueOf(kq));
                    }
                    String[] tieuDe = {"Năm", "Doanh thu"};
                    dtm.setColumnIdentifiers(tieuDe);
                    hienThiDoanhThu(ds);
                } catch (SQLException ex) {
                    Logger.getLogger(frmThongKe.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else if (btn == rdSach.getModel()) {
                ArrayList<String> dsS = new ArrayList<>();
                try {
                    dsS = ThongKeDAO.thongKeSachBanChay();
                    String tieuDe = "Mã sách - Tên sách - Tên tác giả - Giá bán - SLKho";
                    dtm.setColumnIdentifiers(new Vector<>());
                    dtm.addColumn(tieuDe);
                    hienThiTKKH_S(dsS);
                } catch (SQLException ex) {
                    Logger.getLogger(frmThongKe.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else if (btn == rdKH.getModel()) {
                ArrayList<String> dsKH = new ArrayList<>();
                try {
                    dsKH = ThongKeDAO.thongKeKhachHangMuaNhieu();
                    String tieuDe = "Mã khách hàng - Tên khách hàng - SDT - Tổng tiền đã mua";
                    dtm.setColumnIdentifiers(new Vector<>());
                    dtm.addColumn(tieuDe);
                    hienThiTKKH_S(dsKH);
                } catch (SQLException ex) {
                    Logger.getLogger(frmThongKe.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }

    }//GEN-LAST:event_btnTKActionPerformed

