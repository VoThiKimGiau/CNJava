    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (txt_maTacGia.getText().isEmpty() || txt_tenTacGia.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập đầy đủ thông tin.");
            return; // Dừng thực hiện nếu có trường trống
        }

        // Kiểm tra trùng khóa
        if (TacGiaDAO.isAuthorExists(txt_maTacGia.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Mã tác giả đã tồn tại.");
            return; // Dừng thực hiện nếu mã tác giả đã tồn tại
        }
        TacGia newObj = new TacGia(txt_maTacGia.getText(), txt_tenTacGia.getText());
        if (TacGiaDAO.addAuthor(newObj)) {
            JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
            LoadData();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Thêm thất bại");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        TacGia newObj = new TacGia(txt_maTacGia.getText(), txt_tenTacGia.getText());
        if (TacGiaDAO.updateAuthor(newObj)) {
            JOptionPane.showMessageDialog(rootPane, "Sửa thành công");
            LoadData();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Sửa thất bại");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (TacGiaDAO.deleteAuthor(txt_maTacGia.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Xóa thành công");
            LoadData();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Xóa thất bại");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            String maTacGia = (String) jTable1.getValueAt(selectedRow, 0);
            String tenTacGia = (String) jTable1.getValueAt(selectedRow, 1);

            // Gán giá trị vào các ô text
            txt_maTacGia.setText(maTacGia);
            txt_tenTacGia.setText(tenTacGia);
            txt_maTacGia.enable(false);
        }
    }//GEN-LAST:event_jTable1MouseClicked

