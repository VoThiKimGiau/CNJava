    private void LoadData() {
        try {
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Mã tác giả");
            model.addColumn("Tên tác giả");

            List<TacGia> ds = TacGiaDAO.getAllAuthors();

            for (TacGia tg : ds) {
                Object[] row = {
                    tg.getMaTG(),
                    tg.getTenTG()
                };
                model.addRow(row);
            }

            // Set model cho jTable1
            jTable1.setModel(model);
        } catch (Exception ex) {
            ex.printStackTrace();
            // Xử lý lỗi nếu cần
        }
        txt_maTacGia.setText("");
        txt_tenTacGia.setText("");
        txt_maTacGia.enable(true);
    }

