    private void btnDeleteNCCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteNCCActionPerformed
        // TODO add your handling code here:

        int row = tbNhaCungCap.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn hàng cần xóa");
        } else {
            int n = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không?", "Cảnh báo", JOptionPane.YES_NO_OPTION);
            if (n == JOptionPane.NO_OPTION) {
                return;
            } else {
                if (NhaCungCapDAO.deleteNCC(txtMaNCC.getText().toString())) {
                    dsNCC = NhaCungCapDAO.layDSNCC();
                    JOptionPane.showMessageDialog(this, "Xóa thành công");
                } else {
                    JOptionPane.showMessageDialog(this, "Xóa thất bại.\nNếu muốn xóa nhà cung cấp phải xóa các phiếu nhập từ nhà cung cấp đó. Vui lòng thử lại");
                }
                hienThi(dsNCC);
            }
        }
    }//GEN-LAST:event_btnDeleteNCCActionPerformed

    private void btnSearchNCCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchNCCActionPerformed
        // TODO add your handling code here:
        txtMaNCC.setText("");
        txtTenNCC.setText("");
        txtDiaChiNCC.setText("");
        txtEmailNCC.setText("");

        txtTenNCC.enable(false);
        txtDiaChiNCC.enable(false);
        txtEmailNCC.enable(false);
        txtSDTNCC.enable(true);
        txtSDTNCC.requestFocus();

        if (txtSDTNCC.getText().isEmpty())
            JOptionPane.showMessageDialog(this, "Vui lòng nhập số điện thoại");
        else {
            if (kiemTraNhap(1)) {
                ArrayList<NhaCungCap> lst = new ArrayList<>();
                NhaCungCap ncc = NhaCungCapDAO.searchSDT(txtSDTNCC.getText().toString());
                if (ncc.getMaNCC() == null) {
                    JOptionPane.showMessageDialog(this, "Không có nhà cung cấp có số điện thoại này!");
                } else {
                    lst.add(ncc);
                    hienThi(lst);
                }
            }
        }
    }//GEN-LAST:event_btnSearchNCCActionPerformed

    private void btnSaveNCCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveNCCActionPerformed
        // TODO add your handling code here:
        hienThi(dsNCC);
        NhaCungCap ncc = new NhaCungCap();
        ncc.setTenNCC(txtTenNCC.getText().toString());
        ncc.setDiaChi(txtDiaChiNCC.getText().toString());
        ncc.setSdt(txtSDTNCC.getText().toString());
        ncc.setEmail(txtEmailNCC.getText().toString());

        if (txtMaNCC.getText().isEmpty()) {
            if (kiemTraNhap(0)) {
                if (NhaCungCapDAO.themNCC(ncc)) {
                    dsNCC = NhaCungCapDAO.layDSNCC();
                    JOptionPane.showMessageDialog(this, "Thêm nhà cung cấp thàng công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm nhà cung cấp thất bại. Vui lòng thử lại", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        } else {
            if (kiemTraNhap(0)) {
                ncc.setMaNCC(txtMaNCC.getText().toString());
                if (NhaCungCapDAO.updateNCC(ncc)) {
                    dsNCC = NhaCungCapDAO.layDSNCC();
                    JOptionPane.showMessageDialog(this, "Cập nhật nhà cung cấp thàng công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Cập nhật nhà cung cấp thất bại. Vui lòng thử lại", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }

        hienThi(dsNCC);
    }//GEN-LAST:event_btnSaveNCCActionPerformed

