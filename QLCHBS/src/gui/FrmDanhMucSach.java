    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        txtMaDanhMuc.setEnabled(false);
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            String maDanhMuc = (String) jTable1.getValueAt(selectedRow, 0);
            String tenDanhMuc = (String) jTable1.getValueAt(selectedRow, 1);

            // Gán giá trị vào các ô text
            txtMaDanhMuc.setText(maDanhMuc);
            txtTenDanhMuc.setText(tenDanhMuc);
        }
    }//GEN-LAST:event_jTable1MouseClicked

