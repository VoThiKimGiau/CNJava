    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        hienThi(dsCT);
        ChiTietPhieuNhap ct = new ChiTietPhieuNhap();

        ct.setMaPhieu(cboMaPhieu.getSelectedItem().toString());
        String ma = ChiTietPhieuDAO.layMaSachTuCBO(cboTenSach.getSelectedItem().toString());
        ct.setMaSach(ma);

        if (!ChiTietPhieuDAO.ktrCTPN(ct.getMaPhieu(), ct.getMaSach())) {
            if (ktNhap(0)) {
                ct.setSoLuong(Integer.parseInt(txtSoLuong.getText().toString()));
                ct.setGiaNhap(Float.parseFloat(txtGiaNhap.getText().toString()));

                if (ChiTietPhieuDAO.themCTPN(ct)) {
                    dsCT = ChiTietPhieuDAO.layDSCTPN();
                    JOptionPane.showMessageDialog(this, "Thêm chi tiết phiếu nhập thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm chi tiết phiếu nhập thất bại. Vui lòng thử lại", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        } else {
            if (ktNhap(0)) {
                ct.setSoLuong(Integer.parseInt(txtSoLuong.getText().toString()));
                ct.setGiaNhap(Float.parseFloat(txtGiaNhap.getText().toString()));

                int n = JOptionPane.showConfirmDialog(this, "Bạn đã thêm cuốn sách này ở phiếu nhập này rồi nên chỉ có thể cập nhật lại số lượng hoặc giá tiền\n Chọn Yes nếu đồng ý sửa và No khi không cần", "Thông báo", JOptionPane.YES_NO_OPTION);
                if (n == JOptionPane.YES_OPTION) {
                    if (ChiTietPhieuDAO.updateCTPN(ct)) {
                        dsCT = ChiTietPhieuDAO.layDSCTPN();
                        JOptionPane.showMessageDialog(this, "Cập nhật chi tiết phiếu nhập thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(this, "Cập nhật chi tiết phiếu nhập thất bại. Vui lòng thử lại", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    }
                }
            }
        }

        hienThi(dsCT);
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tbCTPN.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn hàng cần xóa");
        } else {
            int n = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không?", "Cảnh báo", JOptionPane.YES_NO_OPTION);
            if (n == JOptionPane.NO_OPTION) {
                return;
            } else {
                String maPhieu = cboMaPhieu.getSelectedItem().toString();
                String maSach = ChiTietPhieuDAO.layMaSachTuCBO(cboTenSach.getSelectedItem().toString());

                if (ChiTietPhieuDAO.deleteCTPN(maPhieu, maSach)) {
                    dsCT = ChiTietPhieuDAO.layDSCTPN();
                    JOptionPane.showMessageDialog(this, "Xóa thành công");
                } else {
                    JOptionPane.showMessageDialog(this, "Xóa thất bại. Vui lòng thử lại");
                }
                hienThi(dsCT);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        cboTenSach.setEnabled(false);
        txtSoLuong.setEnabled(false);
        txtGiaNhap.setEnabled(false);
        cboMaPhieu.setEnabled(true);

        if (ktNhap(1)) {
            String maPN = cboMaPhieu.getSelectedItem().toString();
            ArrayList<ChiTietPhieuNhap> lst = ChiTietPhieuDAO.searchMaPN(maPN);
            if (lst.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Không có chi tiết phiếu nhập của phiếu nhập này!");
            } else {
                hienThi(lst);
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

