    public void hienThi(ArrayList<NhanVien> dsNV) {
        dtm.setRowCount(0);
        for (NhanVien nv : dsNV) {
            Vector<Object> vec = new Vector<Object>();
            vec.add(nv.getMaNV());
            vec.add(nv.getTenNV());
            vec.add(nv.getDiaChi());
            vec.add(nv.getSdt());
            vec.add(nv.getChucVu());
            dtm.addRow(vec);
        }
    }

    public boolean kiemTraNhap(int loai) {
        boolean kq = true;
        String sdt = txtSDT.getText().toString();
        KhachHang kh = KhachHangDAO.searchSDT(sdt);

        if (sdt.isEmpty()) {
            kq = false;
            JOptionPane.showMessageDialog(this, "Số điện thoại không được bỏ trống. Vui lòng nhập lại!");
            txtSDT.requestFocus();
        } else if (sdt.matches(".*\\D.*")) {
            kq = false;
            JOptionPane.showMessageDialog(this, "Số điện thoại không được có chữ. Vui lòng nhập lại!");
            txtSDT.requestFocus();
        } else if (!sdt.substring(0, 1).equals("0")) {
            kq = false;
            JOptionPane.showMessageDialog(this, "Số điện thoại không bắt đầu bằng 0. Vui lòng nhập lại!");
            txtSDT.requestFocus();
        } else if (sdt.length() < 10) {
            kq = false;
            JOptionPane.showMessageDialog(this, "Số điện thoại không đủ 10 số. Vui lòng nhập lại!");
            txtSDT.requestFocus();
        } else if (sdt.length() > 10) {
            kq = false;
            JOptionPane.showMessageDialog(this, "Số điện thoại hơn 10 số. Vui lòng nhập lại!");
            txtSDT.requestFocus();
        } else if (kh.getMaKH() != null && txtMaNV.getText().toString().isEmpty() && loai == 0) {// loại = 0 là không phải tìm kiếm
            kq = false;
            JOptionPane.showMessageDialog(this, "Số điện thoại đã tồn tại. Vui lòng nhập lại!");
            txtSDT.requestFocus();
        } else if (txtTenNV.getText().isEmpty() && loai == 0) {// loại = 0 là không phải tìm kiếm
            kq = false;
            JOptionPane.showMessageDialog(this, "Không được bỏ trống tên nhân viên. Vui lòng nhập lại!");
            txtTenNV.requestFocus();
        } else if (txtDiaChi.getText().toString().isEmpty() && loai == 0) {
            kq = false;
            txtDiaChi.requestFocus();
            JOptionPane.showMessageDialog(this, "Địa chỉ không được để trống. Vui lòng nhập lại!");
            return kq;
        }

        return kq;
    }

