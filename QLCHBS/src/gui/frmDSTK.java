    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        hienThi(dsNV);
        DangNhap dn = new DangNhap();
        String id = txtMaNV.getText().toString().strip();
        dn.setMaNV(txtMaNV.getText().toString().strip());
        dn.setTenDN(txtTK.getText().toString().strip());
        dn.setMK(txtMK.getText().toString().strip());
        dn.setTenND(cboCV.getSelectedItem().toString().strip());

        if (DangNhapDao.KtraID(id)) {
            JOptionPane.showMessageDialog(this, "Mã nhân viên đã tồn tại");
            txtMaNV.setText("");
            txtMaNV.requestFocus();
        } else if (!DangNhapDao.KtraNVTuNhanVien(id)) {
            JOptionPane.showMessageDialog(this, "Chưa có nhân viên này. Vui lòng thêm nhân viên trước khi thêm tài khoản");
        } else if (txtTK.getText().toString().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập tài khoản");
            txtTK.requestFocus();
        } else if (txtMK.getText().toString().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập mật khẩu");
            txtMK.requestFocus();
        } else if (DangNhapDao.themNV(dn)) {
            System.out.println("1");
            dsNV = DangNhapDao.layDSDN();
            JOptionPane.showMessageDialog(this, "Thêm tài khoản thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Thêm tài khoản đăng nhập thất bại. Vui lòng thử lại", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
        hienThi(dsNV);
    }//GEN-LAST:event_btnSaveActionPerformed

