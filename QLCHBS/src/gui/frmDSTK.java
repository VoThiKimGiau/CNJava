    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:

        int row = tbNV.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn hàng cần xóa");
        } else {
            int n = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không?", "Cảnh báo", JOptionPane.YES_NO_OPTION);
            if (n == JOptionPane.NO_OPTION) {
                return;
            } else {
                if (DangNhapDao.deleteNV(txtMaNV.getText().toString())) {
                    dsNV = DangNhapDao.layDSDN();
                    JOptionPane.showMessageDialog(this, "Xóa thành công");
                    txtMaNV.setText("");
                    txtTK.setText("");
                    txtMK.setText("");
                    cboCV.setEnabled(false);
                    txtMaNV.setEnabled(false);
                    txtTK.setEnabled(false);
                    txtMK.setEnabled(false);
                } else {
                    JOptionPane.showMessageDialog(this, "Xóa thất bại. Vui lòng thử lại");
                }
                hienThi(dsNV);
            }
        }


    }//GEN-LAST:event_btnDeleteActionPerformed

