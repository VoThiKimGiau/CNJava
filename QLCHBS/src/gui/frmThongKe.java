    public void hienThiDoanhThu(ArrayList<String> ds) {
        dtm.setRowCount(0);

        for (int i = 0; i < ds.size(); i += 2) {
            Vector<Object> vec = new Vector<Object>();
            vec.add(ds.get(i));

            if (i + 1 < ds.size()) {
                vec.add(ds.get(i + 1));
            } else {
                vec.add(""); // Nếu không có phần tử thứ hai, thêm một giá trị trống vào cột thứ hai
            }

            dtm.addRow(vec);
        }
    }

    public void hienThiTKKH_S(ArrayList<String> ds) {
        dtm.setRowCount(0);

        for (String x : ds) {
            Vector<Object> vec = new Vector<Object>();
            vec.add(x);
            dtm.addRow(vec);
        }
    }

