    public void hienThi(ArrayList<PhieuNhap> ds) {
        dtm.setRowCount(0);
        for (PhieuNhap pn : ds) {
            Vector<Object> vec = new Vector<Object>();
            vec.add(pn.getMaPhieu());
            String tenNCC = PhieuNhapDAO.layTenNCC(pn.getMaNCC());
            vec.add(tenNCC);
            vec.add(pn.getMaNV());
            vec.add(pn.getNgayNhap());
            vec.add(pn.getTongTien());

            dtm.addRow(vec);
        }
    }

    public boolean ktNhap() {
        boolean kq = true;
        String pattern = "\\d{4}-\\d{2}-\\d{2}";
        String input = txtNgayNhap.getText();

        boolean isMatch = input.matches(pattern);
        if (!isMatch) {
            kq = false;
            JOptionPane.showMessageDialog(this, "Nhập sai định dạng ngày.\nĐịnh dạng:yyyy-mm-dd.\nVui lòng nhập lại!");
        }
        return kq;
    }

