    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        txt_maSach.setEnabled(true);
        // Kiểm tra trường trống
        if (txt_maSach.getText().isEmpty() || txt_tenSach.getText().isEmpty()
                || txt_giaBan.getText().isEmpty() || txt_sl.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập đầy đủ thông tin.");
            return; // Dừng thực hiện nếu có trường trống
        }

        // Kiểm tra trùng khóa
        if (SachDAO.isBookExists(txt_maSach.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Mã sách đã tồn tại.");
            return; // Dừng thực hiện nếu mã sách đã tồn tại
        }
        Sach newObj = new Sach(txt_maSach.getText(), getMaTacGia(),
                getMaDanhMuc(), Double.parseDouble(txt_giaBan.getText()), Integer.parseInt(txt_sl.getText()),
                txt_tenSach.getText());
        if (SachDAO.addBook(newObj)) {
            JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
            LoadData();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Thêm thất bại");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

