    private void btnluuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnluuActionPerformed
        txtMaDanhMuc.setEnabled(true);
        String maDanhMuc = txtMaDanhMuc.getText();
        String tenDanhMuc = txtTenDanhMuc.getText();
        if (maDanhMuc.isEmpty() || tenDanhMuc.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập đầy đủ thông tin", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Kiểm tra trùng khóa
        if (DanhMucSachDAO.isCategoryExists(maDanhMuc)) {
            JOptionPane.showMessageDialog(rootPane, "Mã danh mục đã tồn tại", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        DanhMucSach newObj = new DanhMucSach(txtMaDanhMuc.getText(), txtTenDanhMuc.getText());
        if (DanhMucSachDAO.addCategory(newObj)) {
            JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
            LoadData();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Thêm thất bại");
        }

    }//GEN-LAST:event_btnluuActionPerformed

