    public void hienThi(ArrayList<DangNhap> dsDN) {
        dtm.setRowCount(0);
        for (DangNhap kh : dsDN) {
            Vector<Object> vec = new Vector<Object>();
            vec.add(kh.getMaNV());
            vec.add(kh.getTenND());
            vec.add(kh.getTenDN());
            dtm.addRow(vec);
        }
    }

