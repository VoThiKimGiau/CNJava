    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tbPhieuNhap.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn hàng cần xóa");
        } else {
            int n = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không?", "Cảnh báo", JOptionPane.YES_NO_OPTION);
            if (n == JOptionPane.NO_OPTION) {
                return;
            } else {
                if (PhieuNhapDAO.deletePN(txtMaPhieu.getText().toString())) {
                    dsPN = PhieuNhapDAO.layDSPN();
                    JOptionPane.showMessageDialog(this, "Xóa thành công");
                } else {
                    JOptionPane.showMessageDialog(this, "Xóa thất bại\nNếu muốn xóa phiếu nhập phải xóa các chi tiết phiếu nhập của phiếu nhập đó. Vui lòng thử lại");
                }
                hienThi(dsPN);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        cboNCC.setEnabled(false);
        txtNgayNhap.setEnabled(true);

        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String ngayNhapStr = txtNgayNhap.getText();

        if (txtNgayNhap.getText().isEmpty())
            JOptionPane.showMessageDialog(this, "Vui lòng nhập ngày nhập");
        else {
            if (ktNhap()) {
                try {
                    ArrayList<PhieuNhap> lst = PhieuNhapDAO.searchNgayNhap(ngayNhapStr);
                    if (lst.isEmpty()) {
                        JOptionPane.showMessageDialog(this, "Không có phiếu nhập vào ngày này!");
                    } else {
                        hienThi(lst);
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(frmPhieuNhap.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        hienThi(dsPN);
        PhieuNhap pn = new PhieuNhap();
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        try {
            Date ngayNhap = dateFormat.parse(txtNgayNhap.getText().toString());
            pn.setNgayNhap(ngayNhap);
        } catch (ParseException e) {
            e.printStackTrace();
        }
        pn.setTongTien(Float.parseFloat(txtTongTien.getText().toString()));
        String ma = PhieuNhapDAO.layMaNCCTuCbo(cboNCC.getSelectedItem().toString());
        pn.setMaNCC(ma);

        if (txtMaPhieu.getText().isEmpty()) {
            if (ktNhap()) {
                if (PhieuNhapDAO.themPN(pn)) {
                    dsPN = PhieuNhapDAO.layDSPN();
                    JOptionPane.showMessageDialog(this, "Thêm phiếu nhập thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm phiếu nhập thất bại. Vui lòng thử lại", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        } else {
            if (ktNhap()) {
                pn.setMaPhieu(txtMaPhieu.getText().toString());
                if (PhieuNhapDAO.updatePN(pn)) {
                    dsPN = PhieuNhapDAO.layDSPN();
                    JOptionPane.showMessageDialog(this, "Cập nhật phiếu nhập thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Cập nhật phiếu nhập thất bại. Vui lòng thử lại", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }

        hienThi(dsPN);
    }//GEN-LAST:event_btnSaveActionPerformed

