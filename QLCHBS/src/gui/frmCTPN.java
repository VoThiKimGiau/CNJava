    public void hienThi(ArrayList<ChiTietPhieuNhap> ds) {
        dtm.setRowCount(0);
        for (ChiTietPhieuNhap ct : ds) {
            Vector<Object> vec = new Vector<Object>();
            vec.add(ct.getMaPhieu());
            String tenSach = ChiTietPhieuDAO.layTenSach(ct.getMaSach().toString());
            vec.add(tenSach);
            vec.add(ct.getSoLuong());
            vec.add(ct.getGiaNhap());

            dtm.addRow(vec);
        }
    }

    public boolean ktNhap(int x) { // x == 1 là tìm kiếm
        boolean kq = true;
        if (x != 1) {
            if (txtSoLuong.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Số lượng không được bỏ trống. Vui lòng nhập lại");
                kq = false;
            } else if (txtSoLuong.getText().toString().matches(".*\\D.*")) {
                kq = false;
                JOptionPane.showMessageDialog(this, "Số lượng không được có chữ. Vui lòng nhập lại!");
                txtSoLuong.requestFocus();
            } else if (Integer.parseInt(txtSoLuong.getText().toString()) <= 0) {
                JOptionPane.showMessageDialog(this, "Số lượng không được nhỏ hơn hoặc bằng 0. Vui lòng nhập lại");
                kq = false;
            } else if (txtGiaNhap.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Giá nhập không được bỏ trống. Vui lòng nhập lại");
                kq = false;
            } else if (txtGiaNhap.getText().toString().contains(",")) {
                JOptionPane.showMessageDialog(this, "Hãy sử dụng dấu (.) thay vì (,). Vui lòng nhập lại");
                kq = false;
            } else if (Float.parseFloat(txtGiaNhap.getText().toString()) <= 0) {
                JOptionPane.showMessageDialog(this, "Giá nhập không được nhỏ hơn hoặc bằng 0. Vui lòng nhập lại");
                kq = false;
            }
        }

        return kq;
    }

